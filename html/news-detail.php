			<h2 align="center">
    纯靠卖包装的古树茶会被消费者遗弃?
</h2>
<p align="center">
    <span style="color:#7f7f7f">2014-08-22 17:52:03&nbsp;&nbsp;中国普洱茶网<br/></span>
</p>
<p>
    　　中国普洱茶网讯：普洱古树茶价格突飞猛涨，会不会导致2007年崩盘再现？对此，业内人士表示不必担心。
</p>
<p>
    　　中国普洱茶网讯：自2010年起，普洱茶逐步走出“寒冰期”，呈现量价齐升的回暖态势。近几年，以班章、冰岛等产地为代表的古树茶，受到发烧友、游资的热捧，价格连年快速走高。与往年相比，今年景迈山、冰岛、班章、昔归等几个地方出产的普洱古树茶备受追捧，2万元/公斤的价格随处可见。
</p>
<p>
    　　古树茶被热炒，会不会引发普洱茶的二次崩盘危机？对此，济南茶界人士认为，古树茶炒作目前属于少数人的游戏，越热炒越能加速洗牌，最终让没有品质的古树茶退出市场。
</p>
<p>
    　　<strong>1、包单株采纯料，发烧友热炒古树茶</strong>
</p>
<p>
    　　“古树茶炒作有点过火，现在包单株古树茶的人相对较多，把价格炒了上来，”梵益茶创始人、云南臻味号山东总代理祝永国称，“通常情况下，茶农会把不同茶树的叶子采下来统一制作，但一些古树茶发烧友讲究纯料，而单株古树的茶叶纯度最高，所以这些人就直接到老班章这样的村子里，看好一棵古树，押上万元钱，让茶农专门采摘这个古树上的茶叶。不过，单株茶产量很少，一年一棵树就产五六斤左右，树林非常大的古树也禁止过度采摘”。
</p>
<p>
    　　另一位业界人士称，从2008年到现在普洱古树茶一直在涨价，涨价最快的是冰岛和老班章的古树茶，为古树茶捧场的多为地产公司老板，这些发烧友每年都会赶到产地高价收购古树茶，看到难得的好茶，都是谁出价高村民就卖给谁。但他认为，这种热捧并非人为炒作，而属于市场正常调节。“树上茶叶本来仅是农副产品，有市场需求才能卖高价，有的人就是喜欢、适合喝这种茶叶而且喜欢收藏，玩的就是这个过程”。
</p>
<p>
    　　<strong>2、古树茶产量很少，假货多扰乱市场</strong>
</p>
<p>
    　　古树茶受热捧，这种茶的产量有多少呢？有业内人士说，在整个普洱茶中，古树茶一年产量非常少，最多占10%，还有人认为，100斤普洱毛茶中仅产0.9斤古树茶。
</p>
<p>
    　　“在市面上销售的古树茶中，真古树茶最多占20%，大部分是假货。”祝永国称，“古树茶本身品质不错，但现在市面上假货太多，很多茶饼包装棉纸上印的是古树茶字样，里面却是一般的普洱茶。”
</p>
<p>
    　　他说，古树茶甜度好，汤色澄黄透亮，叶底饱满有韧性，不像假货叶底很碎、不整状，而且古树茶的苦涩味转化快，入口很快回甘，喝起来饱满，又能冲泡二三十遍，而台地茶（树龄短的普洱茶）喝起来味道比较寡淡。“不过，由于普洱茶大多压成饼销售，从外形看不大出区别，老百姓选购的话一定要先试喝，别盲目相信外包装。”
</p>
<p>
    　　也有业内人士表示，选购古树茶要认准正规品牌，“每个做品牌的厂家不可能砸自己的牌子”。
</p>
<p>
    　　<strong>3、古树茶投资专业性强外行人别盲目跟风</strong>
</p>
<p>
    　　“我认识的商界朋友，有不少人拿出1亿元以上存茶叶。对有钱人来说，用1万元买一片茶饼相当于老百姓花100元买一片。”一位茶商告诉记者。他说，古树茶投资挺专业，能玩得了古树茶投资的都是茶商、有钱的发烧友，普通消费者没有资金实力也缺乏鉴别力，还是少凑热闹为妙。
</p>
<p>
    　　业内人士称，刚入门的老百姓选购普洱茶可先从大众消费的档次起步，六七百元、千元左右一斤的品质就很不错了，两三万元一斤的先不必考虑。
</p>
<p>
    　　<strong>4、越热炒越加速洗牌没品质的古树茶将出局</strong>
</p>
<p>
    　　普洱古树茶价格突飞猛涨，会不会导致2007年崩盘再现？对此，业内人士表示不必担心。
</p>
<p>
    　　“今年普洱古树茶虽然炒得有点火，但限于局部，大多数的普洱茶涨幅不是太高，”祝永国称，“今年古树茶热炒也算件好事，能彻底加速洗牌的进度。毕竟消费者现在比较理性，品饮多了，也会越来越懂行。只有坚持做品质好的古树茶，消费者的信赖度才会越来越高，单纯靠卖包装的古树茶会被消费者遗弃”。
</p>
